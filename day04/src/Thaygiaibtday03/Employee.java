package Thaygiaibtday03;

public class Employee {
	public int id;
	public String name;
	public double Salary;
	public int age;
	public boolean Married;
	Job job;

	public Employee(int id, String name, double salary, int age, boolean married, Job job) {
		super();
		this.id = id;
		this.name = name;
		Salary = salary;
		this.age = age;
		Married = married;
		this.job = job;
	}

}

package Day3;

public class Employee {
	public int id;
	public String name;
	public double Salary;
	public int age;
	public boolean Married;
	Job job;
	public Employee(int id, String name, double salary, Job job) {
		super();
		this.id = id;
		this.name = name;
		Salary = salary;
		this.job = job;
	}

}
